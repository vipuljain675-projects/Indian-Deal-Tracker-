// ── Main ──

func main() {
	// Check terraform installed first
	if !checkTerraform() {
		fmt.Println(red("\n❌ Terraform not installed!"))
		fmt.Println(yellow("   Fix: brew install terraform"))
		fmt.Println(grey("   Then rerun this command."))
		os.Exit(1)
	}

	// Try to auto-load .env.local so user doesn't have to export everything
	// Look in current dir and two levels up (from devops-go/deals-infra/)
	envPaths := []string{
		".env.local",
		"../../.env.local",
		"../../../.env.local",
	}
	for _, path := range envPaths {
		if _, err := os.Stat(path); err == nil {
			readEnvFile(path)
			break
		}
	}

	// Get command
	args := os.Args[1:]
	if len(args) == 0 {
		cmdHelp()
		return
	}
	command := args[0]

	// Help and status don't need full config
	switch command {
	case "help", "--help", "-h":
		cmdHelp()
		return
	case "status":
		cmdStatus()
		return
	case "destroy":
		cmdDestroy()
		return
	}

	// All other commands need full config
	cfg, missing := loadConfig()

	if len(missing) > 0 && command != "generate" {
		fmt.Println(red("\n❌ Missing required environment variables:"))
		for _, m := range missing {
			fmt.Printf("   %s %s\n", red("✗"), m)
		}
		fmt.Println(grey("\n   These are in your .env.local — export them first:"))
		fmt.Println(grey("   export VERCEL_API_TOKEN='your_token'"))
		fmt.Println(grey("   export VERCEL_PROJECT_ID='prj_xxxx'"))
		fmt.Println(grey("   export VERCEL_ORG_ID='team_xxxx'"))
		fmt.Println(grey("   export GITHUB_REPO='owner/repo'"))
		fmt.Println(grey("\n   Run 'go run main.go help' for where to find each value."))
		os.Exit(1)
	}

	switch command {
	case "deploy":
		cmdDeploy(cfg)
	case "plan":
		cmdPlan(cfg)
	case "generate":
		// Use placeholders if IDs not set — useful for previewing the .tf
		if cfg.VercelProjectID == "" {
			cfg.VercelProjectID = "YOUR_PROJECT_ID"
		}
		if cfg.VercelOrgID == "" {
			cfg.VercelOrgID = "YOUR_ORG_ID"
		}
		if cfg.GitRepo == "" {
			cfg.GitRepo = "owner/repo-name"
		}
		cmdGenerate(cfg)
	default:
		fmt.Printf("\n%s '%s'\n", red("❌ Unknown command:"), command)
		cmdHelp()
	}
}